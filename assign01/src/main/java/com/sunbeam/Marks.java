package com.sunbeam;

public class Marks {
	String subject;
	double marks;
	
	public Marks(String subject, double d) {
		
		this.subject = subject;
		this.marks = d;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public double getMarks() {
		return marks;
	}
	public void setMarks(float marks) {
		this.marks = marks;
	}
	

}
